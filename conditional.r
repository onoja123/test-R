a <- 33
b <- 200

# Using if only
if (b > a) {
  print("b is greater than a")
}

# Using if and else if
if (b > a) {
  print("b is greater than a")
} else if (a == b) {
  print ("a and b are equal")
}

# Using if, else if and else
if (b > a) {
  print("b is greater than a")
} else if (a == b) {
  print("a and b are equal")
} else {
  print("a is greater than b")
}
